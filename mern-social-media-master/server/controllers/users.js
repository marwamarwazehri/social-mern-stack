import User from "../models/User.js";

/* READ */
export const getUser = async (req, res) => {
  try {
    const { id } = req.params;
    const user = await User.findById(id);
    res.status(200).json(user);
  } catch (err) {
    res.status(404).json({ message: err.message });
  }
};

export const getUserFriends = async (req, res) => {
  try {
    const { id } = req.params;
    const user = await User.findById(id);

    const friends = await Promise.all(
      user.friends.map((id) => User.findById(id))

      /*-Promise.all is called with this array of promises. 
      It waits for all the promises to resolve, and it returns a new promise.
      
      -When all the promises in the array have resolved 
      (i.e., when all the user documents have been successfully found by their IDs), 
      the new promise returned by Promise.all resolves with an array of the 
      resolved user documents. This array is stored in the friends variable.
      
      */
    );
    const formattedFriends = friends.map( /*hon 3am nzabet lformat ta3olet lfriends 3ashen ne2der neb3aton la lfrontend */
      ({ _id, firstName, lastName, occupation, location, picturePath }) => {
        return { _id, firstName, lastName, occupation, location, picturePath };
      }
    );
    res.status(200).json(formattedFriends);
  } catch (err) {
    res.status(404).json({ message: err.message });
  }
};

